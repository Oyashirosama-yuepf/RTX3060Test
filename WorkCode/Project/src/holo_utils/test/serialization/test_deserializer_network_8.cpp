#include <gtest/gtest.h>

#include <holo/core/types.h>
#include <holo/serialization/deserializer.h>

using Deserializer    = holo::serialization::Deserializer<true, 8>;

struct TestPoint
{
    uint32_t x;
    uint32_t y;
    
    template <typename D>
    void Deserialize(D& deserializer)
    {
        deserializer >> x >> y;
    }
};

struct TestObject
{
    uint8_t          a;
    uint16_t         b;
    uint32_t         c;
    uint64_t         d;
    holo::float32_t  e;
    holo::float64_t  f;
    uint16_t         g;
    uint64_t         h;
    uint8_t          i;
    holo::float32_t  j;
    uint8_t          k;
    std::string      l;

    template <typename D>
    void Deserialize(D& deserializer)
    {
        deserializer >> a >> b >> c >> d >> e >> f >> g >> h >> i >> j >> k >> l;
    }
};

TEST(Deserializer, constructor)
{
    uint8_t buffer[1024];
    Deserializer       deser(buffer, 1024);
    ASSERT_EQ(0, deser.GetOffset());
    ASSERT_EQ(1024, deser.GetSize());
    ASSERT_EQ(buffer, deser.GetData());
}

